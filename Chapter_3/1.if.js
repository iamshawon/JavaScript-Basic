/* 1. If Condition
============================================*/

var a = 10;
var b = 20;

// Greater Less
if (a > b) {
  console.log(a + " is greater than" + b);
}
if (a < b) {
  console.log(b + " is greater than " + a);
}

// Even Odd
var n = 5;
if (n % 2 === 0) {
  console.log(n + " is a Even number");
}
if (n % 2 === 1) {
  console.log(n + " is a Odd number");
}
