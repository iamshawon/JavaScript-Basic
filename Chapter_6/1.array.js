// Topic: Array declaration
//==================================================
// Types: Array Literal , Constructor

var arr = [1, 2, 3, 4, 5];
console.log(arr);
console.log(arr[0]);

// new data entry
arr[5] = 6;
console.log(arr);

arr[10] = 11;
console.log(arr);
console.log(arr[7]);

// array length
console.log(arr.length);

// modify data
arr[2] = 22;
console.log(arr);
